#include<stdio.h>

int main(){

    int a=10;
    int b=20;

    //Addition
    printf("sum=%d\n",a+b);

    //Subtraction
    printf("sum=%d\n",a-b);

    //multiplication
    printf("sum=%d\n",a*b);

    //divide 
    printf("sum=%d\n",a/b);

    //modul
    printf("sum=%d\n",11%2);

    //Increment Operator ++
     int num=10;
    printf("num=%d\n",num);
    num++; //num=num+1;
    printf("num++=%d\n",num);

    //Decremet Operator --
    int num1=10;
    printf("num=%d\n",num1);
    num1--;
    printf("num=%d",num1);

    return 0;
}