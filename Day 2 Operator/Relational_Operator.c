#include<stdio.h>

int main(){
/**
 * ==
 * !=
 * > greater than
 * < less than
 * <= less than
 * >= greater than
 */
    
    int a=10;
    int b=20;
    int c=10;
    //equal to Operator(==)
    printf("sum=%d\n",a==b);
    printf("sum=%d\n",a==c);

    //Not equal to Operator(!=)
    printf("sum=%d\n",a!=b);
    printf("sum=%d\n",a!=c);

    //Geater than(>)
    print("sum=%d\n",a>b);
    printf("sum=%d\n",a>c);

    //Less than(<)
    printf("sum=%d\n",a<b);
    printf("sum=%d\n",a<c);

    //Greater than equal to 
    printf("sum=%d\n",a>=b);
    printf("sum=%d\n",a>=c);

    //Less than equal to 
    printf("sum=%d\n",a<=b);
    printf("sum=%d\n",a<=c);

    

    return 0;
}